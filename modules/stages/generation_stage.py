def _write_xyz(mol, conf_id, path, engine, energy=None, energy_units="kcal/mol"):
    """Write a single conformer to XYZ format with standardised comment line."""
    conf  = mol.GetConformer(conf_id)
    atoms = list(mol.GetAtoms())
    comment = (
        f"Generated by GenerationStage | engine={engine} | "
        f"E={energy:.8f} {energy_units}"
        if energy is not None
        else f"Generated by GenerationStage | engine={engine}"
    )
    with open(path, "w") as f:
        f.write(f"{len(atoms)}\n{comment}\n")
        for atom in atoms:
            pos = conf.GetAtomPosition(atom.GetIdx())
            f.write(f"{atom.GetSymbol()} {pos.x:.6f} {pos.y:.6f} {pos.z:.6f}\n")


def _write_xyz_from_lines(natoms, atom_lines, path, engine, energy=None, energy_units="Ha"):
    """Write a conformer to XYZ format from raw atom lines (used by CREST parser)."""
    comment = (
        f"Generated by GenerationStage | engine={engine} | "
        f"E={energy:.8f} {energy_units}"
        if energy is not None
        else f"Generated by GenerationStage | engine={engine}"
    )
    with open(path, "w") as f:
        f.write(f"{natoms}\n{comment}\n")
        for line in atom_lines:
            f.write(line + "\n")


def _parse_multimodel_xyz(path):
    """
    Parse a multi-model XYZ file into a list of
    (natoms, comment_line, atom_lines) tuples.
    """
    result = []
    try:
        with open(path) as f:
            lines = [l.rstrip("\n") for l in f]
    except Exception:
        return result
    i = 0
    while i < len(lines):
        line = lines[i].strip()
        if not line:
            i += 1
            continue
        try:
            natoms = int(line)
        except ValueError:
            i += 1
            continue
        comment    = lines[i + 1] if i + 1 < len(lines) else ""
        atom_lines = lines[i + 2: i + 2 + natoms]
        if len(atom_lines) == natoms:
            result.append((natoms, comment, atom_lines))
        i += 2 + natoms
    return result


def _split_crest_xyz(crest_xyz, inchi_key, smiles, xyz_out_dir,
                     timestamp, source_row, charge, multiplicity, gfn_mode, crest_version):
    """
    Parse crest_conformers.xyz (multi-model XYZ) and write individual
    conformer XYZ files.  Returns list of ConformerRecord.to_dict().
    """
    records = []
    try:
        with open(crest_xyz) as f:
            lines = [l.rstrip("\n") for l in f]
    except Exception:
        return records

    i = conf_num = 0
    n_lines = len(lines)

    while i < n_lines:
        try:
            natoms = int(lines[i].strip())
        except ValueError:
            i += 1
            continue
        if i + 1 >= n_lines:
            break

        comment_line = lines[i + 1]
        atom_lines   = lines[i + 2: i + 2 + natoms]
        if len(atom_lines) < natoms:
            break

        try:
            energy = float(comment_line.strip())
        except ValueError:
            energy = None

        xyz_path = os.path.join(xyz_out_dir, f"{inchi_key}_conf{conf_num:03d}.xyz")
        _write_xyz_from_lines(
            natoms      = natoms,
            atom_lines  = atom_lines,
            path        = xyz_path,
            engine      = f"crest_{gfn_mode}",
            energy      = energy,
            energy_units = "Ha",
        )

        record = _make_record(
            inchi_key = inchi_key,
            conf_num  = conf_num,
            xyz_path  = xyz_path,
            energy    = energy,
            smiles    = smiles,
            provenance = {
                "backend":              "crest",
                "crest_version":        crest_version,
                "gfn":                  gfn_mode,
                "generation_timestamp": timestamp,
                "source_row":           source_row,
                "charge":               charge,
                "multiplicity":         multiplicity,
            },
        )
        records.append(record.to_dict())
        conf_num += 1
        i += 2 + natoms

    return records


def _make_record(inchi_key, conf_num, xyz_path, energy, smiles, provenance):
    """
    Build a ConformerRecord and append the required optimisation_history entry.
    Returns the ConformerRecord instance.
    """
    record = ConformerRecord(
        inchi_key  = inchi_key,
        conf_num   = conf_num,
        xyz_path   = xyz_path,
        energy     = energy,
        smiles     = smiles,
        provenance = provenance,
    )
    record.optimisation_history.append({
        "stage":     "generation",
        "engine":    provenance.get("backend", "unknown"),
        "energy":    energy,
        "xyz_path":  xyz_path,
        "timestamp": provenance.get("generation_timestamp", ""),
    })
    return record


