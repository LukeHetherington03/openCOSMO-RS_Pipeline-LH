"""
modules/stages/generation_stage.py

Conformer generation stage.  Strictly generation only — no optimisation,
no pruning, no forcefield minimisation beyond what is intrinsic to the
generator (e.g. RDKit MMFF energy evaluation for sorting purposes).

=========================================================================
INPUT
=========================================================================

  stage_input : cleaned.csv from CleaningStage
    Required columns: inchi_key, smiles
    Optional: charge, mol_name

=========================================================================
OUTPUT  (canonical stage output)
=========================================================================

  energies.json   — ConformerSet JSON
    One entry per generated conformer.

=========================================================================
AUXILIARY OUTPUTS
=========================================================================

  summary.csv
  generation_metadata.json
  xyz/<inchi_key>_conf<NNN>.xyz

=========================================================================
BACKENDS
=========================================================================

  rdkit      — ETKDGv3 embedding + MMFF94/UFF energy evaluation
               (energy is the FF energy at the generated geometry, not an
                optimisation — geometry is not updated)
  crest      — Single RDKit seed → CREST iMTD-GC conformer search
               Energy read from XYZ comment line (absolute GFN2-xTB Ha).
               Requires CREST v2 executable (flag-based API).
  openbabel  — RDKit seed → obabel CLI --conformer GA
               MMFF94 energy evaluated after generation via RDKit.
               Requires obabel on PATH or configured via config["openbabel"]["executable"].

=========================================================================
PARALLELISM
=========================================================================

  Uses BaseStage.run_parallel() for all backends.
  n_workers and cores_per_item injected by PipelineRunner, then adjusted
  in execute() using generation_defaults.json:

    cores_per_item[engine]  caps cores_per_item allocated by resource allocator
    parallel[engine]        if false, forces n_workers=1 (effectively serial)

  All backends run as module-level worker functions (_worker_rdkit etc.)
  dispatched through the single picklable entry point _generation_worker().

=========================================================================
ADDING A BACKEND
=========================================================================

  1. Add entry: GENERATION_BACKENDS["mybackend"] = "_worker_mybackend"
  2. Add dispatch branch in _generation_worker()
  3. Implement module-level _worker_mybackend(args: dict) -> dict
     args contains (from _base_args + _build_args):
       item_id, inchi_key, smiles, charge, multiplicity, seed, cores_per_item,
       num_confs, xyz_out_dir, workdir, log_path, checkpoints_dir,
       timestamp, stage, outputs_dir,
       <plus any tool paths, e.g. crest_exe, obabel_exe>
     Must return:
       item_id, status ("ok"|"failed"), log_details (list of (k,v) tuples),
       worker_name, worker_pid,
       conformers (list of ConformerRecord.to_dict()),
       error (str, only when status=="failed")
  4. Add cores_per_item and parallel entries in generation_defaults.json
  5. Add exe resolution in GenerationStage._build_args() if needed
  6. Add version detection in GenerationStage._get_tool_versions() if needed

=========================================================================
ITEM TRACKING
=========================================================================

  Items are inchi_keys (one per unique molecule in the CSV).
  Multiple rows with the same inchi_key are collapsed to one item.

=========================================================================
STRICT MODE
=========================================================================

  Hierarchy (highest wins):
    1. parameters["strict"]              runtime override
    2. generation_defaults.json "strict" stage default
    3. False                             fallback

  When True, any per-molecule failure aborts the pool immediately.

=========================================================================
XYZ COMMENT LINE FORMAT
=========================================================================

  All XYZ files written by this stage use the format:
    Generated by GenerationStage | engine=<engine> | E=<energy> <units>

  For CREST, the energy is the absolute GFN2-xTB energy in Hartree.
  For RDKit and OpenBabel, the energy is the MMFF94/UFF energy in kcal/mol.

"""

