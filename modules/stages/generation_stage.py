# Module-level entry point — single picklable worker dispatched by run_parallel
# =============================================================================

def _generation_worker(args: dict) -> dict:
    """
    Dispatch to the correct backend worker based on args["engine"].
    This is the only function submitted to ProcessPoolExecutor.
    """
    engine = args.get("engine", "rdkit")
    if engine == "rdkit":
        return _worker_rdkit(args)
    if engine == "crest":
        return _worker_crest(args)
    if engine == "openbabel":
        return _worker_openbabel(args)
    return {
        "item_id":     args.get("item_id", "unknown"),
        "status":      "failed",
        "error":       f"Unknown generation engine: '{engine}'",
        "log_details": [],
        "worker_name": multiprocessing.current_process().name,
        "worker_pid":  os.getpid(),
        "conformers":  [],
    }


