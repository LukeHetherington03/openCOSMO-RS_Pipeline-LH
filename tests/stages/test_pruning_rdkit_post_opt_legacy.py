"""
tests/stages/test_pruning_rdkit_post_opt_legacy.py

Unit tests for PruningStage._prune_rdkit_post_opt_legacy().

Verifies that the 4 IC-pipeline filters are applied correctly:
  1. Convergence  — remove if MMFF status != "converged"
  2. Overlap      — remove if any atom pair < 0.1 Å
  3. Boltzmann    — remove if rel_prob < 1% at 298.15 K (~2.73 kcal/mol cutoff)
  4. RMSD 1.0 Å   — cluster, lower-energy conformer survives

Run with:
    cd /home/lunet/cglh4/openCOSMO-RS_Pipeline-LH
    pytest tests/stages/test_pruning_rdkit_post_opt_legacy.py -v
"""

import math
import os
import textwrap
from unittest.mock import MagicMock, call, patch

import pytest

from modules.stages.pruning_stage import PruningStage
from modules.utils.conformers import ConformerRecord


# ─────────────────────────────────────────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────────────────────────────────────────

RT_KCAL = 8.314 * 298.15 / 4184.0   # ≈ 0.5927 kcal/mol


def _boltzmann_cutoff_kcal():
    """Maximum ΔE that survives the 1% Boltzmann cutoff (rel_prob ≥ 0.01)."""
    return -math.log(0.01) * RT_KCAL   # ≈ 2.73 kcal/mol


def _make_stage() -> PruningStage:
    """Return a PruningStage instance with log methods mocked out."""
    stage = PruningStage.__new__(PruningStage)
    stage.log_info    = MagicMock()
    stage.log_warning = MagicMock()
    stage.log_error   = MagicMock()
    return stage


def _make_rec(
    tmp_path,
    conf_num: int,
    energy: float,
    status: str = "converged",
    xyz_content: str | None = None,
) -> ConformerRecord:
    """
    Build a minimal ConformerRecord with an XYZ file on disk.

    Default XYZ is a 3-atom water-like triangle with normal geometry.
    """
    if xyz_content is None:
        xyz_content = textwrap.dedent("""\
            3
            Generated by test
            O  0.000  0.000  0.000
            H  0.957  0.000  0.000
            H -0.240  0.927  0.000
        """)

    xyz_path = tmp_path / f"conf{conf_num:03d}.xyz"
    xyz_path.write_text(xyz_content)

    return ConformerRecord(
        inchi_key="TESTMOLECULE-UHFFFAOYSA-N",
        conf_num=conf_num,
        xyz_path=str(xyz_path),
        energy=energy,
        smiles="O",
        optimisation_history=[
            {"stage": "optimisation", "engine": "forcefield_mmff", "status": status}
        ],
    )


# ─────────────────────────────────────────────────────────────────────────────
# Convergence filter
# ─────────────────────────────────────────────────────────────────────────────

class TestConvergenceFilter:

    def test_removes_partial(self, tmp_path):
        stage = _make_stage()
        recs = [
            _make_rec(tmp_path, 0, 1.0, status="converged"),
            _make_rec(tmp_path, 1, 2.0, status="partial"),
        ]
        result = stage._prune_rdkit_post_opt_legacy(recs, enabled=True)
        ids = [r.conf_num for r in result]
        assert 0 in ids
        assert 1 not in ids

    def test_removes_failed(self, tmp_path):
        stage = _make_stage()
        recs = [
            _make_rec(tmp_path, 0, 1.0, status="converged"),
            _make_rec(tmp_path, 1, 2.0, status="failed"),
        ]
        result = stage._prune_rdkit_post_opt_legacy(recs, enabled=True)
        assert len(result) == 1
        assert result[0].conf_num == 0

    def test_keeps_all_converged(self, tmp_path):
        stage = _make_stage()
        recs = [_make_rec(tmp_path, i, float(i), status="converged") for i in range(3)]
        # Bypass RMSD — all 3 share the same default XYZ, so without mocking
        # the RMSD filter would collapse them to 1. We test convergence only here.
        with patch.object(stage, '_prune_rmsd', side_effect=lambda c, rmsd_threshold: c):
            result = stage._prune_rdkit_post_opt_legacy(recs, enabled=True)
        assert len(result) == 3

    def test_no_history_treated_as_unknown(self, tmp_path):
        stage = _make_stage()
        rec = _make_rec(tmp_path, 0, 1.0)
        rec.optimisation_history = []
        result = stage._prune_rdkit_post_opt_legacy([rec], enabled=True)
        assert result == []

    def test_disabled_returns_all(self, tmp_path):
        stage = _make_stage()
        recs = [_make_rec(tmp_path, i, float(i), status="failed") for i in range(3)]
        result = stage._prune_rdkit_post_opt_legacy(recs, enabled=False)
        assert len(result) == 3


